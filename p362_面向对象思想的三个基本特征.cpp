/*
面向对象思想的三个基本特征是什么？
	封装。封装将客观事物抽象成类，每个类对自身的数据和方法实行访问控制，
通过关键字(private, protected 和 public)来控制外界对类成员的访问，
以达到保护数据和方法的目的。
	继承。子类从父类继承，从而获得父类的属性和方法。广义的继承有三种实现形式：
实现继承(指使用基类的属性和方法而无须额外的编码能力)、可视继承(子窗体使用父窗体的
外观和实现代码)、接口继承(仅使用属性和方法，实现滞后到子类实现)。前两种(类继承)
和后一种(对象组合成接口继承及纯虚函数)构成功能复用的两种方式。
	多态。多态将父对象设置成和一个或更多的它的子对象相等的技术，赋值之后，父对象就可以
根据当前赋值给它的子对象的特性以不同的方式运作。简单来说就是一句话：允许将子类类型
的指针赋值给父类类型的指针。
*/